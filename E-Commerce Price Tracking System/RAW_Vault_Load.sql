insert into VAULT.hub_customer 
(CUSTOMER_HK,
    CUSTOMER_ID,
    LOAD_DATE,
    RECORD_SOURCE )
    SELECT
    MD5(UPPER(TRIM(CUSTOMER_ID))) AS CUSTOMER_HK,
    CUSTOMER_ID,
    CURRENT_TIMESTAMP(),
    'CRM_SYSTEM'
FROM STAGE_DEV.STG_CUSTOMERS;




