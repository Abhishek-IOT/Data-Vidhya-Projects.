CREATE OR REPLACE PROCEDURE SP_LOAD_DATA_VAULT_HUBS()
RETURNS STRING
LANGUAGE SQL
AS
$$
BEGIN
insert into VAULT.hub_customer 
(CUSTOMER_HK,
    CUSTOMER_ID,
    LOAD_DATE,
    RECORD_SOURCE )
    SELECT
    MD5(UPPER(TRIM(CUSTOMER_ID))) AS CUSTOMER_HK,
    CUSTOMER_ID,
    CURRENT_TIMESTAMP(),
    'CRM_SYSTEM'
FROM STAGE_DEV.STG_CUSTOMERS;




insert into VAULT.HUB_PRODUCT 
(PRODUCT_HK,
    PRODUCT_ID,
    LOAD_DATE,
    RECORD_SOURCE )
    SELECT
    MD5(UPPER(TRIM(PRODUCT_ID))) AS PRODUCt_HK,
    PRODUCT_ID,
    CURRENT_TIMESTAMP(),
    'CRM_SYSTEM'
FROM STAGE_DEV.STG_PRODUCTs;


insert into VAULT.HUB_ORDER 
(ORDER_HK,
    ORDER_ID,
    LOAD_DATE,
    RECORD_SOURCE )
    SELECT
    MD5(UPPER(TRIM(ORDER_ID))) AS ORDER_HK,
    ORDER_ID,
    CURRENT_TIMESTAMP(),
    'CRM_SYSTEM'
FROM STAGE_DEV.STG_ORDERs;



insert into VAULT.HUB_SELLER 
(SELLER_HK,
    SELLER_ID,
    LOAD_DATE,
    RECORD_SOURCE )
    SELECT
    MD5(UPPER(TRIM(SELLER_ID))) AS SELLER_HK,
    SELLER_ID,
    CURRENT_TIMESTAMP(),
    'CRM_SYSTEM'
FROM STAGE_DEV.STG_SELLERS;



insert into VAULT.HUB_PROMOTION
(PROMOTION_HK,
    PROMOTION_ID,
    LOAD_DATE,
    RECORD_SOURCE )
    SELECT
    MD5(UPPER(TRIM(PROMOTION_ID))) AS PROMOTION_HK,
    PROMOTION_ID,
    CURRENT_TIMESTAMP(),
    'CRM_SYSTEM'
FROM STAGE_DEV.STG_PROMOTIONs;
 RETURN 'SUCCESS';

END;
$$;



INSERT INTO LINK_CUSTOMER_ORDER (
    CUSTOMER_ORDER_HK,
    CUSTOMER_HK,
    ORDER_HK,
    LOAD_DATE,
    RECORD_SOURCE
)
SELECT
    MD5(HP.CUSTOMER_HK || '|' || HO.ORDER_HK),
 HP.CUSTOMER_HK,
    HO.ORDER_HK,

    CURRENT_TIMESTAMP(),
    'ORDER_SYSTEM'

FROM (
    SELECT DISTINCT
        ORDER_ID,
        CUSTOMER_ID
    FROM STAGE_DEV.STG_ORDERS
    WHERE ORDER_ID IS NOT NULL
      AND CUSTOMER_ID IS NOT NULL
) S

JOIN HUB_ORDER HO
    ON HO.ORDER_HK = MD5(UPPER(TRIM(S.ORDER_ID)))

JOIN HUB_CUSTOMER HP
    ON HP.CUSTOMER_HK = MD5(UPPER(TRIM(S.CUSTOMER_ID)))

WHERE NOT EXISTS (
    SELECT 1
    FROM LINK_CUSTOMER_ORDER L
    WHERE L.CUSTOMER_ORDER_HK =
           MD5(HP.CUSTOMER_HK || '|' || HO.ORDER_HK)
);




