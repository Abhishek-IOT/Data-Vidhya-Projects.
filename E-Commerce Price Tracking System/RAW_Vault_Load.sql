    CITY,
     MD5(
            UPPER(TRIM(COALESCE(CUSTOMER_NAME,''))) || '|' ||
             UPPER(TRIM(COALESCE(phone,''))) || '|' ||
            UPPER(TRIM(COALESCE(EMAIL,''))) || '|' ||
